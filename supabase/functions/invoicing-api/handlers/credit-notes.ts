/**
 * invoicing-api — /credit-notes handlers (Wave 5 / Phase 8).
 *
 * Endpoints:
 *   GET    /credit-notes                  — list
 *   POST   /credit-notes                  — create draft
 *   GET    /credit-notes/:id              — detail
 *   POST   /credit-notes/:id/issue        — draft → issued
 *   POST   /credit-notes/:id/apply        — issued → applied; bumps applied_cents
 *                                            and stamps invoice_id if not set
 *   POST   /credit-notes/:id/void         — any non-voided → voided (stamps voided_at)
 *
 * Drifts from API-contract §6 (DB-wins reconcile; docs reconcile in 5.4):
 *   - DB has no `void_reason` column on credit_notes (verified 2026-05-15).
 *     The handler accepts a free-text reason for the wire/audit surface but
 *     does not persist it on the row; the audit-log row generated by future
 *     triggers (when wired) will carry the caller's reason via request context.
 *     For Wave 5 this is reason-only; persistence pattern lands in Phase 9.
 *   - Schema-master §9.6 notes a "synthetic payments row" semantic for apply,
 *     but `payments.payment_method_id` is a FK (not a text discriminator) and
 *     `payments.amount_cents > 0` CHECK forbids a zero-amount synthetic. So
 *     for Wave 5 the apply handler bumps `credit_notes.applied_cents` and
 *     stamps `invoice_id` only; full invoice rollup of credit-note offsets
 *     lands in Phase 9.
 *   - The DB CHECK `applied_cents <= amount_cents` is the hard floor; the
 *     handler validates server-side before bumping to surface a clean 422.
 */

import type { Ctx } from '../../_shared/route.ts';
import { ok, err, ApiError, fromApiError } from '../../_shared/responses.ts';
import { requireCaller } from '../../_shared/tenant.ts';
import {
  CreditNoteApplySchema,
  CreditNoteCreateSchema,
  CreditNoteIssueSchema,
  CreditNoteSchema,
  CreditNoteVoidSchema,
  type CreditNote,
} from '../../_shared/types.ts';
import { assertTransition, WorkflowError } from '../../_shared/workflow.ts';
import {
  admin,
  decodeCursor,
  paginate,
  parseBody,
  parseLimit,
  requireCap,
  respondWithIdempotency,
  type Caller,
} from '../../_shared/handler-helpers.ts';

const CREDIT_NOTE_COLS =
  'id, org_id, credit_note_number, customer_id, invoice_id, issue_date, status, reason, ' +
  'currency_code, amount_cents, applied_cents, notes, voided_at, created_at, updated_at';

interface CreditNoteRow {
  id: string;
  org_id: string;
  credit_note_number: string;
  customer_id: string;
  invoice_id: string | null;
  issue_date: string;
  status: string;
  reason: string | null;
  currency_code: string;
  amount_cents: number;
  applied_cents: number;
  notes: string | null;
  voided_at: string | null;
  created_at: string;
  updated_at: string;
}

function rowToCreditNote(row: CreditNoteRow): CreditNote {
  return CreditNoteSchema.parse(row);
}

function workflowToApiError(e: unknown): never {
  if (e instanceof WorkflowError) {
    throw new ApiError('STATE_CONFLICT', e.message, 409, {
      machine: e.machine,
      from: e.from,
      to: e.to,
    });
  }
  throw e;
}

async function fetchCreditNoteRow(caller: Caller, id: string): Promise<CreditNoteRow> {
  const { data, error } = await admin()
    .from('credit_notes')
    .select(CREDIT_NOTE_COLS)
    .eq('id', id)
    .eq('org_id', caller.orgId)
    .is('deleted_at', null)
    .maybeSingle();
  if (error) {
    throw new ApiError('INTERNAL_ERROR', 'credit note lookup failed', 500, {
      detail: error.message,
    });
  }
  if (!data) throw new ApiError('NOT_FOUND', 'credit note not found', 404);
  return data as CreditNoteRow;
}

async function nextCreditNoteNumber(orgId: string): Promise<string> {
  const { data, error } = await admin().rpc('next_doc_number', {
    p_org_id: orgId,
    p_doc_type: 'credit_note',
  });
  if (error || typeof data !== 'string') {
    throw new ApiError('INTERNAL_ERROR', 'next_doc_number credit_note failed', 500, {
      detail: error?.message,
    });
  }
  return data;
}

// =========================================================================
// GET /credit-notes
// =========================================================================
export async function listCreditNotes({ req, url }: Ctx): Promise<Response> {
  try {
    const caller = requireCaller(req);
    requireCap(caller, 'credit_notes.read');
    const limit = parseLimit(url);
    const cursor = decodeCursor(url.searchParams.get('cursor'));
    const status = url.searchParams.get('status');
    const customerId = url.searchParams.get('customer_id');
    const invoiceId = url.searchParams.get('invoice_id');
    const currency = url.searchParams.get('currency_code');

    let query = admin()
      .from('credit_notes')
      .select(CREDIT_NOTE_COLS)
      .eq('org_id', caller.orgId)
      .is('deleted_at', null)
      .order('created_at', { ascending: false })
      .order('id', { ascending: false })
      .limit(limit + 1);

    if (status) query = query.eq('status', status);
    if (customerId) query = query.eq('customer_id', customerId);
    if (invoiceId) query = query.eq('invoice_id', invoiceId);
    if (currency) query = query.eq('currency_code', currency);
    if (cursor) {
      query = query.or(
        `created_at.lt.${cursor.created_at},and(created_at.eq.${cursor.created_at},id.lt.${cursor.id})`,
      );
    }

    const { data, error } = await query;
    if (error) {
      return err(
        'INTERNAL_ERROR',
        'credit note list query failed',
        { detail: error.message },
        500,
        { req },
      );
    }
    const rows = (data ?? []) as CreditNoteRow[];
    const { items, next_cursor } = paginate(rows, limit);
    return ok({ items: items.map(rowToCreditNote), next_cursor }, undefined, { req });
  } catch (e) {
    if (e instanceof ApiError) return fromApiError(e, req);
    throw e;
  }
}

