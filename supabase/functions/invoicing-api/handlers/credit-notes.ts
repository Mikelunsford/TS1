// POST /credit-notes/:id/issue
// =========================================================================
export async function issueCreditNote({ req, params }: Ctx): Promise<Response> {
  try {
    const caller = requireCaller(req);
    requireCap(caller, 'credit_notes.issue');
    const body = await parseBody(req, CreditNoteIssueSchema);
    const id = params.id;

    return await respondWithIdempotency(
      req,
      caller,
      'invoicing-api',
      'POST /credit-notes/:id/issue',
      body,
      async () => {
        const existing = await fetchCreditNoteRow(caller, id);
        try {
          assertTransition('credit_note', existing.status, 'issued');
        } catch (e) {
          workflowToApiError(e);
        }
        const nowIso = new Date().toISOString();
        const { data, error } = await admin()
          .from('credit_notes')
          .update({ status: 'issued', updated_at: nowIso })
          .eq('id', id)
          .eq('org_id', caller.orgId)
          .select(CREDIT_NOTE_COLS)
          .single();
        if (error || !data) {
          throw new ApiError('INTERNAL_ERROR', 'credit note issue failed', 500, {
            detail: error?.message,
          });
        }
        return { status: 200, body: { data: rowToCreditNote(data as CreditNoteRow) } };
      },
    );
  } catch (e) {
    if (e instanceof ApiError) return fromApiError(e, req);
    throw e;
  }
}

// =========================================================================
// POST /credit-notes/:id/apply
// =========================================================================
export async function applyCreditNote({ req, params }: Ctx): Promise<Response> {
  try {
    const caller = requireCaller(req);
    requireCap(caller, 'credit_notes.apply');
    const body = await parseBody(req, CreditNoteApplySchema);
    const id = params.id;

    return await respondWithIdempotency(
      req,
      caller,
      'invoicing-api',
      'POST /credit-notes/:id/apply',
      body,
      async () => {
        const existing = await fetchCreditNoteRow(caller, id);
        try {
          assertTransition('credit_note', existing.status, 'applied');
        } catch (e) {
          workflowToApiError(e);
        }

        // Verify target invoice is in caller's org + currency matches the CN.
        const { data: invoice, error: iErr } = await admin()
          .from('invoices')
          .select('id, currency_code, customer_id, status')
          .eq('id', body.invoice_id)
          .eq('org_id', caller.orgId)
          .is('deleted_at', null)
          .maybeSingle();
        if (iErr) {
          throw new ApiError('INTERNAL_ERROR', 'invoice lookup failed', 500, {
            detail: iErr.message,
          });
        }
        if (!invoice) {
          throw new ApiError('VALIDATION_ERROR', 'invoice_id not found in caller org', 422);
        }
        const inv = invoice as { currency_code: string; customer_id: string; status: string };
        if (inv.currency_code !== existing.currency_code) {
          throw new ApiError(
            'VALIDATION_ERROR',
            `credit note currency ${existing.currency_code} does not match invoice ${inv.currency_code}`,
            422,
          );
        }
        if (inv.customer_id !== existing.customer_id) {
          throw new ApiError(
            'VALIDATION_ERROR',
            'invoice customer does not match credit_note customer',
            422,
          );
        }

        const newApplied = existing.applied_cents + body.amount_cents;
        if (newApplied > existing.amount_cents) {
          throw new ApiError(
            'VALIDATION_ERROR',
            `applied_cents ${newApplied} would exceed credit_note.amount_cents ${existing.amount_cents}`,
            422,
          );
        }

        const fullyApplied = newApplied === existing.amount_cents;
        const nowIso = new Date().toISOString();
        const patch: Record<string, unknown> = {
          applied_cents: newApplied,
          invoice_id: body.invoice_id,
          updated_at: nowIso,
        };
        if (fullyApplied) patch.status = 'applied';

        const { data, error } = await admin()
          .from('credit_notes')
          .update(patch)
          .eq('id', id)
          .eq('org_id', caller.orgId)
          .select(CREDIT_NOTE_COLS)
          .single();
        if (error || !data) {
          throw new ApiError('INTERNAL_ERROR', 'credit note apply failed', 500, {
            detail: error?.message,
          });
        }
        return { status: 200, body: { data: rowToCreditNote(data as CreditNoteRow) } };
      },
    );
  } catch (e) {
    if (e instanceof ApiError) return fromApiError(e, req);
    throw e;
  }
}

