// =========================================================================
// POST /credit-notes
// =========================================================================
export async function createCreditNote({ req }: Ctx): Promise<Response> {
  try {
    const caller = requireCaller(req);
    requireCap(caller, 'credit_notes.write');
    const body = await parseBody(req, CreditNoteCreateSchema);

    return await respondWithIdempotency(
      req,
      caller,
      'invoicing-api',
      'POST /credit-notes',
      body,
      async () => {
        // Verify customer in caller's org.
        const { data: customer, error: cErr } = await admin()
          .from('customers')
          .select('id')
          .eq('id', body.customer_id)
          .eq('org_id', caller.orgId)
          .maybeSingle();
        if (cErr) {
          throw new ApiError('INTERNAL_ERROR', 'customer lookup failed', 500, {
            detail: cErr.message,
          });
        }
        if (!customer) {
          throw new ApiError('VALIDATION_ERROR', 'customer_id not found in caller org', 422);
        }

        // If invoice_id passed, verify it belongs to caller's org and currency
        // matches (credit-notes can only apply against same-currency invoices).
        if (body.invoice_id) {
          const { data: invoice, error: iErr } = await admin()
            .from('invoices')
            .select('id, currency_code, customer_id')
            .eq('id', body.invoice_id)
            .eq('org_id', caller.orgId)
            .is('deleted_at', null)
            .maybeSingle();
          if (iErr) {
            throw new ApiError('INTERNAL_ERROR', 'invoice lookup failed', 500, {
              detail: iErr.message,
            });
          }
          if (!invoice) {
            throw new ApiError('VALIDATION_ERROR', 'invoice_id not found in caller org', 422);
          }
          const inv = invoice as { currency_code: string; customer_id: string };
          if (inv.currency_code !== body.currency_code) {
            throw new ApiError(
              'VALIDATION_ERROR',
              `credit note currency_code ${body.currency_code} does not match invoice ${inv.currency_code}`,
              422,
            );
          }
          if (inv.customer_id !== body.customer_id) {
            throw new ApiError(
              'VALIDATION_ERROR',
              'invoice customer does not match credit_note customer',
              422,
            );
          }
        }

        const cnNumber = await nextCreditNoteNumber(caller.orgId);
        const issue = body.issue_date ?? new Date().toISOString().slice(0, 10);

        const { data, error } = await admin()
          .from('credit_notes')
          .insert({
            org_id: caller.orgId,
            credit_note_number: cnNumber,
            customer_id: body.customer_id,
            invoice_id: body.invoice_id ?? null,
            issue_date: issue,
            status: 'draft',
            reason: body.reason ?? null,
            currency_code: body.currency_code,
            amount_cents: body.amount_cents,
            applied_cents: 0,
            notes: body.notes ?? null,
            created_by: caller.userId,
          })
          .select(CREDIT_NOTE_COLS)
          .single();
        if (error || !data) {
          throw new ApiError('INTERNAL_ERROR', 'credit note insert failed', 500, {
            detail: error?.message,
          });
        }
        return { status: 201, body: { data: rowToCreditNote(data as CreditNoteRow) } };
      },
    );
  } catch (e) {
    if (e instanceof ApiError) return fromApiError(e, req);
    throw e;
  }
}

